<?php
// src/SqlGen/Generator.php

namespace MySqlSchemaSync\SqlGen;

use MySqlSchemaSync\Config\Connection;
use MySqlSchemaSync\Diff\DiffResult;
use MySqlSchemaSync\Diff\Schema;

class Generator
{
    public function __construct(
        private Connection $source,
        private Connection $target,
        private ?Schema $sourceSchema = null,
        private ?Schema $targetSchema = null,
    ) {}

    public function generate(DiffResult $diff, bool $useTransaction = true): string
    {
        $lines = [];
        $lines[] = "-- Auto-generated by MySQL SchemaSync PHP Edition";
        $lines[] = "-- Source: {$this->source->name} ({$this->source->database})";
        $lines[] = "-- Target: {$this->target->name} ({$this->target->database})";
        $lines[] = "-- Generated at: " . date('Y-m-d H:i:s');
        $lines[] = "";

        if ($useTransaction) {
            $lines[] = "START TRANSACTION;";
            $lines[] = "";
        }

        // New tables
        if ($diff->newTables) {
            $lines[] = "-- === 新增表 ===";
            foreach ($diff->newTables as $t) {
                $lines[] = "-- [{$t['risk']}] CREATE TABLE `{$t['name']}`";
                $lines[] = $this->buildCreateTable($t['name']);
                $lines[] = "";
            }
        }

        // Changed tables
        foreach ($diff->changedTables as $t) {
            $lines[] = "-- === 变更表: {$t['name']} === [{$t['risk']}]";
            $alters = [];
            foreach ($t['changes'] as $change) {
                $col = $change['column'] ?? '';
                switch ($change['kind']) {
                    case 'ADD_COLUMN':
                        $def = $this->renderColumnDef($change['detail']);
                        $alters[] = "  ADD COLUMN `{$col}` {$def}";
                        break;
                    case 'DROP_COLUMN':
                        $alters[] = "  DROP COLUMN `{$col}`";
                        break;
                    case 'MODIFY_COLUMN':
                        $detail = $change['changes']['columnType']['to'] ?? '';
                        $alters[] = "  MODIFY COLUMN `{$col}` {$detail}";
                        break;
                }
            }
            if ($alters) {
                $lines[] = "ALTER TABLE `{$t['name']}`";
                $lines[] = implode(",\n", $alters) . ";";
            }
            $lines[] = "";
        }

        // Removed tables
        if ($diff->removedTables) {
            $lines[] = "-- === 删除表 ===";
            foreach ($diff->removedTables as $t) {
                $lines[] = "-- [{$t['risk']}] DROP TABLE `{$t['name']}`";
                $lines[] = "DROP TABLE `{$t['name']}`;";
            }
            $lines[] = "";
        }

        // Indexes
        foreach ($diff->newIndexes as $idx) {
            $colList = $this->lookupIndexColumns($idx['table'], $idx['name']);
            $lines[] = "-- [{$idx['risk']}] ADD INDEX `{$idx['name']}` on `{$idx['table']}`";
            $lines[] = "CREATE INDEX `{$idx['name']}` ON `{$idx['table']}` ({$colList});";
        }
        foreach ($diff->removedIndexes as $idx) {
            $lines[] = "-- [{$idx['risk']}] DROP INDEX `{$idx['name']}` on `{$idx['table']}`";
            $lines[] = "DROP INDEX `{$idx['name']}` ON `{$idx['table']}`;";
        }

        // Foreign keys
        foreach ($diff->newForeignKeys as $fk) {
            $fkDef = $this->lookupForeignKeyDef($fk['table'], $fk['name']);
            $lines[] = "-- [{$fk['risk']}] ADD FK `{$fk['name']}` on `{$fk['table']}`";
            $lines[] = $fkDef;
        }
        foreach ($diff->removedForeignKeys as $fk) {
            $lines[] = "-- [{$fk['risk']}] DROP FK `{$fk['name']}` on `{$fk['table']}`";
            $lines[] = "ALTER TABLE `{$fk['table']}` DROP FOREIGN KEY `{$fk['name']}`;";
        }

        // Triggers
        foreach ($diff->newTriggers as $x) {
            $def = $this->lookupTriggerDef($x['name']);
            $lines[] = "-- [{$x['risk']}] CREATE TRIGGER `{$x['name']}`";
            $lines[] = $def;
        }
        foreach ($diff->removedTriggers as $x) {
            $lines[] = "-- [{$x['risk']}] DROP TRIGGER `{$x['name']}`";
            $lines[] = "DROP TRIGGER IF EXISTS `{$x['name']}`;";
        }

        // Views
        foreach ($diff->newViews as $x) {
            $def = $this->lookupObjectDef('views', $x['name']);
            $lines[] = "-- [{$x['risk']}] CREATE VIEW `{$x['name']}`";
            $lines[] = $def ?: "CREATE VIEW `{$x['name']}` ...;";
        }
        foreach ($diff->removedViews as $x) {
            $lines[] = "-- [{$x['risk']}] DROP VIEW `{$x['name']}`";
            $lines[] = "DROP VIEW IF EXISTS `{$x['name']}`;";
        }

        // Stored Procedures
        foreach ($diff->newProcedures as $x) {
            $def = $this->lookupObjectDef('procedures', $x['name']);
            $lines[] = "-- [{$x['risk']}] CREATE PROCEDURE `{$x['name']}`";
            $lines[] = $def ? "DELIMITER //\n{$def} //\nDELIMITER ;" : "CREATE PROCEDURE `{$x['name']}` ...;";
        }
        foreach ($diff->removedProcedures as $x) {
            $lines[] = "-- [{$x['risk']}] DROP PROCEDURE `{$x['name']}`";
            $lines[] = "DROP PROCEDURE IF EXISTS `{$x['name']}`;";
        }

        // Functions
        foreach ($diff->newFunctions as $x) {
            $def = $this->lookupObjectDef('functions', $x['name']);
            $lines[] = "-- [{$x['risk']}] CREATE FUNCTION `{$x['name']}`";
            $lines[] = $def ? "DELIMITER //\n{$def} //\nDELIMITER ;" : "CREATE FUNCTION `{$x['name']}` ...;";
        }
        foreach ($diff->removedFunctions as $x) {
            $lines[] = "-- [{$x['risk']}] DROP FUNCTION `{$x['name']}`";
            $lines[] = "DROP FUNCTION IF EXISTS `{$x['name']}`;";
        }

        // Events
        foreach ($diff->newEvents as $x) {
            $def = $this->lookupObjectDef('events', $x['name']);
            $lines[] = "-- [{$x['risk']}] CREATE EVENT `{$x['name']}`";
            $lines[] = $def ?: "CREATE EVENT `{$x['name']}` ...;";
        }
        foreach ($diff->removedEvents as $x) {
            $lines[] = "-- [{$x['risk']}] DROP EVENT `{$x['name']}`";
            $lines[] = "DROP EVENT IF EXISTS `{$x['name']}`;";
        }

        if ($useTransaction) {
            $lines[] = "";
            $lines[] = "COMMIT;";
        }

        return implode("\n", $lines);
    }

    private function buildCreateTable(string $tableName): string
    {
        if (!$this->sourceSchema || !isset($this->sourceSchema->tables[$tableName])) {
            return "CREATE TABLE `{$tableName}` (...); -- TODO: columns from source";
        }
        $table = $this->sourceSchema->tables[$tableName];
        $colDefs = [];
        foreach ($table['columns'] as $col) {
            $colDefs[] = "  `{$col['name']}` " . $this->renderColumnDef($col);
        }
        // PK
        $pkCols = [];
        foreach ($table['columns'] as $col) {
            if ($col['columnKey'] === 'PRI') {
                $pkCols[] = "`{$col['name']}`";
            }
        }
        if ($pkCols) {
            $colDefs[] = "  PRIMARY KEY (" . implode(', ', $pkCols) . ")";
        }

        $engine = $table['engine'] ?: 'InnoDB';
        $charset = $table['collation'] ?: 'utf8mb4_general_ci';
        $comment = $table['comment'] ? " COMMENT='" . addslashes($table['comment']) . "'" : '';

        $sql = "CREATE TABLE `{$tableName}` (\n" . implode(",\n", $colDefs) . "\n) ENGINE={$engine} DEFAULT CHARSET={$charset}{$comment};";
        return $sql;
    }

    private function lookupIndexColumns(string $table, string $indexName): string
    {
        if (!$this->sourceSchema) return '...';
        $key = $table . '.' . $indexName;
        if (isset($this->sourceSchema->indexes[$key])) {
            return $this->sourceSchema->indexes[$key]['columns'];
        }
        return '...';
    }

    private function lookupForeignKeyDef(string $table, string $fkName): string
    {
        if (!$this->sourceSchema) {
            return "ALTER TABLE `{$table}` ADD CONSTRAINT `{$fkName}` FOREIGN KEY (...) REFERENCES ...;";
        }
        $key = $table . '.' . $fkName;
        if (isset($this->sourceSchema->foreignKeys[$key])) {
            $fk = $this->sourceSchema->foreignKeys[$key];
            return "ALTER TABLE `{$table}` ADD CONSTRAINT `{$fkName}` FOREIGN KEY (`{$fk['column']}`) REFERENCES `{$fk['refTable']}`(`{$fk['refColumn']}`) ON DELETE {$fk['delete']} ON UPDATE {$fk['update']};";
        }
        return "ALTER TABLE `{$table}` ADD CONSTRAINT `{$fkName}` FOREIGN KEY (...) REFERENCES ...;";
    }

    private function lookupTriggerDef(string $name): string
    {
        if (!$this->sourceSchema || !isset($this->sourceSchema->triggers[$name])) {
            return "CREATE TRIGGER `{$name}` ...;";
        }
        $t = $this->sourceSchema->triggers[$name];
        return "CREATE TRIGGER `{$name}` {$t['timing']} {$t['event']} ON `{$t['table']}` FOR EACH ROW {$t['statement']}"; // DELIMITER handled
    }

    private function lookupObjectDef(string $type, string $name): string
    {
        if (!$this->sourceSchema) return '';
        $map = [
            'views'      => 'views',
            'procedures' => 'procedures',
            'functions'  => 'functions',
            'events'     => 'events',
        ];
        $prop = $map[$type] ?? null;
        if (!$prop || !isset($this->sourceSchema->{$prop}[$name])) return '';
        $item = $this->sourceSchema->{$prop}[$name];
        return $item['def'] ?? '';
    }

    private function renderColumnDef(array $col): string
    {
        $def = $col['columnType'];
        if (!$col['nullable']) {
            $def .= ' NOT NULL';
        }
        if ($col['default'] !== null) {
            $val = $col['default'];
            if ($val === 'CURRENT_TIMESTAMP' || $val === 'CURRENT_TIMESTAMP()') {
                $def .= " DEFAULT {$val}";
            } else {
                $def .= " DEFAULT '" . addslashes($val) . "'";
            }
        }
        if ($col['extra']) {
            $extra = strtoupper($col['extra']);
            // libui-ng ON UPDATE looks like "DEFAULT_GENERATED ON UPDATE CURRENT_TIMESTAMP" — just take the ON UPDATE part
            if (str_contains($extra, 'ON UPDATE')) {
                $def .= ' ' . $extra;
            } elseif (str_contains($extra, 'AUTO_INCREMENT')) {
                $def .= ' AUTO_INCREMENT';
            }
        }
        if (!empty($col['comment'])) {
            $def .= " COMMENT '" . addslashes($col['comment']) . "'";
        }
        return $def;
    }
}
